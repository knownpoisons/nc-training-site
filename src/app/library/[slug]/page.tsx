// Parses `[label](url)` markdown-style links inside instruction strings
// and turns them into real anchor tags. Plain text passes through unchanged.
function renderInline(text: string): React.ReactNode[] {
  const parts: React.ReactNode[] = [];
  const linkRe = /\[([^\]]+)\]\(([^)]+)\)/g;
  let lastIndex = 0;
  let m: RegExpExecArray | null;
  let key = 0;
  while ((m = linkRe.exec(text)) !== null) {
    if (m.index > lastIndex) parts.push(renderEmphasis(text.slice(lastIndex, m.index), key++));
    parts.push(
      <a
        key={`link-${key++}`}
        href={m[2]}
        target="_blank"
        rel="noopener noreferrer"
        className="inline-link"
      >
        {m[1]}
      </a>
    );
    lastIndex = m.index + m[0].length;
  }
  if (lastIndex < text.length) parts.push(renderEmphasis(text.slice(lastIndex), key++));
  return parts;
}

// Bold parser for **strong** within a chunk of plain text.
function renderEmphasis(text: string, baseKey: number): React.ReactNode {
  if (!text.includes("**")) return text;
  const parts: React.ReactNode[] = [];
  const boldRe = /\*\*([^*]+)\*\*/g;
  let lastIndex = 0;
  let m: RegExpExecArray | null;
  let i = 0;
  while ((m = boldRe.exec(text)) !== null) {
    if (m.index > lastIndex) parts.push(text.slice(lastIndex, m.index));
    parts.push(<strong key={`b-${baseKey}-${i++}`}>{m[1]}</strong>);
    lastIndex = m.index + m[0].length;
  }
  if (lastIndex < text.length) parts.push(text.slice(lastIndex));
  return <span key={`em-${baseKey}`}>{parts}</span>;
}

interface Props {
  params: Promise<{ slug: string }>;
}

export function generateStaticParams() {
  return getAllPromptSlugs().map((slug) => ({ slug }));
}

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { slug } = await params;
  const p = getPrompt(slug);
  if (!p || p.comingSoon) return { title: "Prompt — NotContent Library" };
  return {
    title: `${p.title} — NotContent Library`,
    description: p.oneLiner,
  };
}

const pad = (n: number) => String(n).padStart(2, "0");

export default async function PromptPage({ params }: Props) {
  const { slug } = await params;
  const p = getPrompt(slug);
  if (!p || p.comingSoon) notFound();

  return (
    <div className="library">
      <LibraryTopbar
        currentNumber={p.number}
        total={PROMPTS.length}
        shareTitle={`${p.title} — NotContent Library`}
      />

      {/* ─── Hero ────────────────────────────────────────────────────── */}
      <section className="hero">
        <div className="eyebrow">
          <span>
            <span className="num">{pad(p.number)}</span>
            &nbsp;/&nbsp; {p.eyebrow}
          </span>
          <span className="rule" />
        </div>
        <h1>{p.title}.</h1>
        <p className="sub">{p.oneLiner}</p>

      </section>

      {/* ─── Hero media (video preferred, image fallback) ────────────── */}
      {(p.heroVideo || p.heroImage) && (
        <section className="sample">
          <figure>
            {p.heroVideo ? (
              <video
                className="sample-img"
                src={p.heroVideo}
                poster={p.heroImage}
                autoPlay
                muted
                loop
                playsInline
                preload="metadata"
                aria-label={`Sample dashboard for ${p.title}`}
              />
            ) : p.heroImage ? (
              <Image
                src={p.heroImage}
                alt={`Sample output for ${p.title}`}
                width={1100}
                height={780}
                priority
                className="sample-img"
              />
            ) : null}
            <figcaption>
              Sample dashboard generated by this prompt. Yours will look like this
              with your prospects, your voice, and your data.
            </figcaption>
          </figure>
        </section>
      )}

      {/* ─── What it does ────────────────────────────────────────────── */}
      <section className="section">
        <div className="hed">
          <span className="num">01</span>
          <h2>What it does.</h2>
          <span className="rule" />
        </div>
        {p.whatItDoes.split("\n\n").map((para, i) => (
          <p key={i}>{para.trim()}</p>
        ))}
      </section>

      {/* ─── When to use ─────────────────────────────────────────────── */}
      <section className="section">
        <div className="hed">
          <span className="num">02</span>
          <h2>When to use it.</h2>
          <span className="rule" />
        </div>
        <ul>
          {p.whenToUse.map((item, i) => (
            <li key={i}>{renderInline(item)}</li>
          ))}
        </ul>
      </section>

      {/* ─── What you'll get ─────────────────────────────────────────── */}
      <section className="section">
        <div className="hed">
          <span className="num">03</span>
          <h2>What you&rsquo;ll get.</h2>
          <span className="rule" />
        </div>
        <ul>
          {p.whatYoullGet.map((item, i) => (
            <li key={i}>{renderInline(item)}</li>
          ))}
        </ul>
      </section>

      {/* ─── Quick-fire instructions ─────────────────────────────────── */}
      <section className="section">
        <div className="hed">
          <span className="num">04</span>
          <h2>Quick-fire instructions.</h2>
          <span className="rule" />
        </div>
        <ol>
          {p.quickFire.map((step, i) => (
            <li key={i}>{renderInline(step)}</li>
          ))}
        </ol>
      </section>

      {/* ─── The prompt(s) ───────────────────────────────────────────── */}
      <section className="prompt-block">
        <div className="hed">
          <span className="num">05</span>
          <h2>{p.phases && p.phases.length > 1 ? "The prompts." : "The prompt."}</h2>
          <span className="rule" />
        </div>

        {p.phases && p.phases.length > 0 ? (
          <PhaseTabs phases={p.phases} />
        ) : p.prompt ? (
          <div className="surface">
            <CopyButton text={p.prompt} label="Copy prompt" />
            <pre>{p.prompt}</pre>
          </div>
        ) : null}
      </section>

      <LibraryFooter />
    </div>
  );
}
